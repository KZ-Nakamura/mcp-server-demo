/**
 * アクションプランナー用のプロンプトテンプレート
 */

/**
 * アクションプランを生成するためのプロンプトテンプレート
 * @param idea ユーザーのアイディア
 * @param context コンテキスト情報（オプション）
 * @param currentDateTime 現在の日時情報（オプション）
 * @returns テンプレート文字列
 */
export function getActionPlanPrompt(idea: string, context?: string, currentDateTime?: any): string {
  // 日時情報の整形
  const dateTimeInfo = currentDateTime ? 
    `## 現在の日時情報
- 現在日時: ${currentDateTime.dateTime || '未指定'}
- 日本語日付: ${currentDateTime.jpDate || '未指定'}
- 曜日: ${currentDateTime.weekday || '未指定'}` : '';

  return `
あなたは「意思決定と実行推進を支援するAIエージェント」です。  
ユーザーから与えられる【アイディア】と【ユーザーコンテキスト】,【現在の日時】をもとに、  
そのアイディアが現実的に実行可能かどうかを判断し、  
必要に応じてRejectを行い、次に取るべき行動（Next Action）とそのスケジュールを設計してください。
その際、必ず以下の出力形式に厳密に従ってください。


## 出力形式
【意思決定結果】

判定：実行可能 / 実行困難（Reject）

【理由】

実現可否に関する判断の根拠を明記してください

【Next Action提案】

行動内容：

実行日時：

実行場所：

メモ：

【Google Calendar登録用情報】

タイトル：

開始時間：（例：YYYY-MM-DD 10:00）

終了時間：（例：YYYY-MM-DD 11:00）

場所：

メモ：


## アイディア
${idea}

## ユーザーコンテキスト
${context}

## 現在の日時
${dateTimeInfo}

---
### 🎯 目標

- アイディアの **実現可能性を評価**
- 必要であれば **Reject(実行不可能)**と明示すること
- **1週間以内にできる具体的行動（Next Action）**のみ を設計
- 出力形式には厳密に従ってください

---

### 🔍 処理手順

1. アイディアとユーザーコンテキストを読み取り、実行可能性を判定する  
2. 判定理由を100字以内で説明する   
3. 実行日時・場所・内容を含むNext Action(1週間以内に実行可能)なものを1つ設計する  
4. Google Calendar登録に必要なイベント情報を出力する


### 🛑 Reject基準

以下のいずれかに該当する場合はRejectしてください：

- 時間・資金・環境が明らかに不足しており、現実的に実行が困難  
- 実行してもユーザーの目的に寄与しない  
- 内容が曖昧・抽象的すぎて具体的な行動に落とし込めない  
- 安全性や倫理性に問題がある  

---

`.trim();
}

/**
 * モック用のアクションプランを生成
 * @param idea ユーザーのアイディア
 * @param context コンテキスト情報（オプション）
 * @param currentDateTime 現在の日時情報（オプション）
 * @returns アクションプランのリスト
 */
export function generateMockActionPlan(idea: string, context?: string): string[] {
  // 実際の実装では、LLMを使ってアイディアを分析し、具体的なアクションに分解する
  return [
    "1. アイディアの詳細と目標を文書化する",
    "2. 必要なリソースと予算をリストアップする",
    "3. 主要なステークホルダーを特定し、連絡を取る",
    "4. 詳細なタイムラインとマイルストーンを作成する",
    "5. リスク分析を実施し、対策を検討する",
    "6. プロジェクト計画書を作成する",
    "7. 初期実装またはプロトタイプを開発する",
    "8. フィードバックを収集し、計画を調整する"
  ];
} 